nome = input('digite algo:')
print('o tipo da da varialvel é:', type(nome))
print('é só espaço:', nome.isspace())
print('é alfanumerico: ', nome.isalnum())
print('é alfabético: ', nome.isalpha())
print('é numerico: ', nome.isnumeric())
print('é decimal: ', nome.isdecimal())
print('é min: ', nome.islower())
print('é Mai: ', nome.isupper())


